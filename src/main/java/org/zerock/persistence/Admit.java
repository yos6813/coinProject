package org.zerock.persistence;

public class Admit {
	int abNo;
	int ab2No;
	String abName;
	String ab2Name;

	public int getAbNo() {
		return abNo;
	}
	public void setAbNo(int abNo) {
		this.abNo = abNo;
	}
	public int getAb2No() {
		return ab2No;
	}
	public void setAb2No(int ab2No) {
		this.ab2No = ab2No;
	}
	public String getAbName() {
		return abName;
	}
	public void setAbName(String abName) {
		this.abName = abName;
	}
	public String getAb2Name() {
		return ab2Name;
	}
	public void setAb2Name(String ab2Name) {
		this.ab2Name = ab2Name;
	}
}
